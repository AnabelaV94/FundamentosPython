'''Escreve um programa em python que solicite três números inteiros ao utilizador e imprima a soma dos mesmos.'''

a = int(input("Introduza o 1º número inteiro\n:"))
b = int(input("Introduza o 2º números inteiro\n:"))
c = int(input("Introduza o 3º números inteiro\n:"))
soma = a + b + c
print(f"A soma dos números introduzidos é {soma}") 