''' Escreve um programa que solicite um número inteiro ao utilizador e caso o mesmo seja 
maior que 20, devolva o resultado da sua divisão por 2.'''

Numero = int(input("Introduza um número inteiro \n:"))

if Numero > 20:
    print (f"O resultado da sua divisão por 2 é {Numero/2}") 
else:
    print (f"O Número é menor que 20") 