'''Implementa um programa que leia dois números e indique se estes são iguais ou 
diferentes.'''

Numero1 = int(input("Introduza o 1º número\n:"))
Numero2 = int(input("Introduza o 2º número\n:"))

if Numero1 == Numero2:
    print(f"Os Números introduzidos são iguais") 
else:
    print(f"Os Números introduzidos são diferentes") 