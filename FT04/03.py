'''Fazer um programa para ler quatro números inteiros e positivos, calcular e devolver a sua
 média. '''
 
Numero1 = int(input("Introduza o 1º número\n:"))
Numero2 = int(input("Introduza o 2º número\n:"))
Numero3 = int(input("Introduza o 3º número\n:"))
Numero4 = int(input("Introduza o 4º número\n:"))

print (f"A média é {(Numero1+Numero2+Numero3+Numero4)/4}")